#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	i;
	int	arr[10];

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	else if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n *= -1;
	}
	i = 0;
	while (n >= 10)
	{
		arr[i++] = n % 10 + '0';
		n /= 10;
	}
	arr[i] = n + '0';
	while (i >= 0)
		ft_putchar_fd(arr[i--], fd);
}
